package if_;

public class Ex04 {
	public static void main(String[] args) {
		int num = 10;
		if(num == 10) {
			System.out.println("num은 10과 같다.");
		}else System.out.println("num은 10이 아니다.");
		System.out.println("다음 문장들 실행!!");
		int num1 = 20;
		if(num1 == 10) {
			System.out.println("num1은 10과 같다.");
		}else System.out.println("num1은 10이 아니다.");
		System.out.println("다음 문장들 실행!!");
}//if else는 if가 참이면 if만 실행 후 else는 실행되지 않는다
	//if else에서 if가 거짓이면 else가 실행이 된다.
	//만약 if다음 바로 else가 와야 문법적으로 오류가 안나기 때문에
	//if문 다음에 바로 else가 와야한다.
}
